#pragma once
#include "esphome.h"

#include <vector>

class ST7701Init : public esphome::Component, public esphome::i2c::I2CDevice {
  static constexpr uint8_t REG_OUTPUT = 0x01;
  static constexpr uint8_t REG_CONFIG = 0x03;

  static constexpr uint8_t PIN_CS  = 0;
  static constexpr uint8_t PIN_SDA = 1;
  static constexpr uint8_t PIN_SCL = 2;
  static constexpr uint8_t PIN_RST = 7;

 public:
  float get_setup_priority() const override { return esphome::setup_priority::HARDWARE; }

  void setup() override {
    ESP_LOGI("st7701_init", "Starting ST7701 init via PCA9554 bit-bang SPI");

    if (!this->write_byte(REG_CONFIG, 0x40)) {
      ESP_LOGE("st7701_init", "PCA9554 not responding");
      this->mark_failed();
      return;
    }

    out_ = 0xFF;
    this->write_byte(REG_OUTPUT, out_);
    esphome::delay(10);

    set_pin(PIN_RST, false);
    esphome::delay(50);
    set_pin(PIN_RST, true);
    esphome::delay(200);

    cmd(0x11, {});
    esphome::delay(120);

    cmd(0xFF, {0x77, 0x01, 0x00, 0x00, 0x10});
    cmd(0xC0, {0x3B, 0x00});
    cmd(0xC1, {0x0D, 0x02});
    cmd(0xC2, {0x21, 0x08});
    cmd(0xCD, {0x08});
    cmd(0xB0, {0x00, 0x11, 0x18, 0x0E, 0x11, 0x06, 0x07, 0x08, 0x07, 0x22, 0x04, 0x12, 0x0F, 0xAA, 0x31, 0x18});
    cmd(0xB1, {0x00, 0x11, 0x19, 0x0E, 0x12, 0x07, 0x08, 0x08, 0x08, 0x22, 0x04, 0x11, 0x11, 0xA9, 0x32, 0x18});

    cmd(0xFF, {0x77, 0x01, 0x00, 0x00, 0x11});
    cmd(0xB0, {0x60});
    cmd(0xB1, {0x30});
    cmd(0xB2, {0x87});
    cmd(0xB3, {0x80});
    cmd(0xB5, {0x49});
    cmd(0xB7, {0x85});
    cmd(0xB8, {0x21});
    cmd(0xC1, {0x78});
    cmd(0xC2, {0x78});
    esphome::delay(20);
    cmd(0xE0, {0x00, 0x1B, 0x02});
    cmd(0xE1, {0x08, 0xA0, 0x00, 0x00, 0x07, 0xA0, 0x00, 0x00, 0x00, 0x44, 0x44});
    cmd(0xE2, {0x11, 0x11, 0x44, 0x44, 0xED, 0xA0, 0x00, 0x00, 0xEC, 0xA0, 0x00, 0x00});
    cmd(0xE3, {0x00, 0x00, 0x11, 0x11});
    cmd(0xE4, {0x44, 0x44});
    cmd(0xE5, {0x0A, 0xE9, 0xD8, 0xA0, 0x0C, 0xEB, 0xD8, 0xA0, 0x0E, 0xED, 0xD8, 0xA0, 0x10, 0xEF, 0xD8, 0xA0});
    cmd(0xE6, {0x00, 0x00, 0x11, 0x11});
    cmd(0xE7, {0x44, 0x44});
    cmd(0xE8, {0x09, 0xE8, 0xD8, 0xA0, 0x0B, 0xEA, 0xD8, 0xA0, 0x0D, 0xEC, 0xD8, 0xA0, 0x0F, 0xEE, 0xD8, 0xA0});
    cmd(0xEB, {0x02, 0x00, 0xE4, 0xE4, 0x88, 0x00, 0x40});
    cmd(0xEC, {0x3C, 0x00});
    cmd(0xED, {0xAB, 0x89, 0x76, 0x54, 0x02, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x20, 0x45, 0x67, 0x98, 0xBA});

    cmd(0xFF, {0x77, 0x01, 0x00, 0x00, 0x00});
    cmd(0x36, {0x00});
    cmd(0x3A, {0x55});
    cmd(0x21, {});
    esphome::delay(120);
    cmd(0x29, {});

    ESP_LOGI("st7701_init", "ST7701 init complete (Waveshare BSP sequence)");
  }

 private:
  uint8_t out_ = 0xFF;

  void set_pin(uint8_t pin, bool val) {
    if (val)
      out_ |= (1 << pin);
    else
      out_ &= ~(1 << pin);
    this->write_byte(REG_OUTPUT, out_);
  }

  void spi_word(uint16_t data) {
    for (int i = 8; i >= 0; i--) {
      bool bit = (data >> i) & 1;
      uint8_t s = out_ & ~(1 << PIN_SCL);
      if (bit) s |= (1 << PIN_SDA); else s &= ~(1 << PIN_SDA);
      this->write_byte(REG_OUTPUT, s);
      s |= (1 << PIN_SCL);
      this->write_byte(REG_OUTPUT, s);
      out_ = s;
    }
  }

  void cmd(uint8_t command, std::vector<uint8_t> params) {
    set_pin(PIN_CS, false);
    spi_word(command);
    for (uint8_t p : params)
      spi_word(0x100 | p);
    set_pin(PIN_CS, true);
  }
};
